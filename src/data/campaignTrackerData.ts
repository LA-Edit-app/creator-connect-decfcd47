export interface Creator {
  id: string;
  name: string;
  avatar: string;
  handle: string;
  platforms: string[];
}

export interface ContentItem {
  id: string;
  type: "image" | "video" | "reel" | "story" | "carousel";
  title: string;
  url?: string;
  thumbnail?: string;
  platform: string;
  status: "draft" | "pending" | "approved" | "published";
  dueDate?: string;
  notes?: string;
  source?: string;
}

